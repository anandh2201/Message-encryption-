    private File showFileDialog(boolean b) {
       JFileChooser fc = new JFileChooser("Choose an image");
        FileFilter filefilter = new FileFilter(){
           private boolean open;
            @Override
            public boolean accept(File file) {
               String name = file.getName().toLowerCase();
                if(open)
                      return file.isDirectory() || name.endsWith(".jpg") || name.endsWith(".jpeg")|| name.endsWith(".png")|| name.endsWith(".bmg");
                return file.isDirectory() || name.endsWith(".bmp")|| name.endsWith(".png");
            }
            

            @Override
            public String getDescription() {
               if(open)
                    return "Image (*.jpg , *.jpeg, *png, *.bmp)";
                return "Image (*.png , *.bmp)"; 
            }
        };
        fc.setAcceptAllFileFilterUsed(false);
        fc.addChoosableFileFilter((javax.swing.filechooser.FileFilter) filefilter);
        
        File file = null;
        boolean open = true;
        if (open && fc.showOpenDialog(this) == fc.APPROVE_OPTION)
            file = fc.getSelectedFile();
        else if(!open && fc.showSaveDialog(this) == fc.APPROVE_OPTION)
            file = fc.getSelectedFile();
        return file;
    } 
    
    
